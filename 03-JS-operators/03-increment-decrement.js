 //Increment - used to increase the number by 1

let num1 = 5;
let num2 = num1++; //post-increments: increase num1 by 1 but not here

console.log(num1);   //6
console.log(num2);  //5

let num3 = ++num1; //pre-increments: increase num1 by 1 now

console.log(num1);   // 7
console.log(num2);  // 5

// Decrement: used to decrease the number by one

let n1 = 10;
let n2 = --n1 * 5;
console.log(n2); //45

let number = 25;
number++;  //25 but increase next time
console.log(number-- * 10); //260


